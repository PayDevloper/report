package com.sumao.report.summary.pojo;


public class SalesSummary {
	private String					sales;
	private String					amount;
	private String					orderQuantity;
	private String					outStockQuantity;
	private int					customerQuantity;
	private String					newlyCustomerQuantity;



	public String getSales() {
		return sales;
	}



	public void setSales(String sales) {
		this.sales = sales;
	}



	public String getAmount() {
		return amount;
	}



	public void setAmount(String amount) {
		this.amount = amount;
	}



	public String getOrderQuantity() {
		return orderQuantity;
	}



	public void setOrderQuantity(String orderQuantity) {
		this.orderQuantity = orderQuantity;
	}



	public String getOutStockQuantity() {
		return outStockQuantity;
	}



	public void setOutStockQuantity(String outStockQuantity) {
		this.outStockQuantity = outStockQuantity;
	}



	public int getCustomerQuantity() {
		return customerQuantity;
	}



	public void setCustomerQuantity(int customerQuantity) {
		this.customerQuantity = customerQuantity;
	}



	public String getNewlyCustomerQuantity() {
		return newlyCustomerQuantity;
	}



	public void setNewlyCustomerQuantity(String newlyCustomerQuantity) {
		this.newlyCustomerQuantity = newlyCustomerQuantity;
	}



}
