package com.sumao.report.depositfee.pojo;


public class DepositFeeElement{
	private String payNum;
	private String minPaymentAmount;
	private String warningValue;
	private String paymentState;
	private String actualPayment;
	private String paymentType;
	private String paymentDate;
	private int id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPayNum() {
		return payNum;
	}
	public void setPayNum(String payNum) {
		this.payNum = payNum;
	}
	public String getMinPaymentAmount() {
		return minPaymentAmount;
	}
	public void setMinPaymentAmount(String minPaymentAmount) {
		this.minPaymentAmount = minPaymentAmount;
	}
	public String getWarningValue() {
		return warningValue;
	}
	public void setWarningValue(String warningValue) {
		this.warningValue = warningValue;
	}
	public String getPaymentState() {
		return paymentState;
	}
	public void setPaymentState(String paymentState) {
		this.paymentState = paymentState;
	}
	public String getActualPayment() {
		return actualPayment;
	}
	public void setActualPayment(String actualPayment) {
		this.actualPayment = actualPayment;
	}
	public String getPaymentType() {
		return paymentType;
	}
	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}
	public String getPaymentDate() {
		return paymentDate;
	}
	public void setPaymentDate(String paymentDate) {
		this.paymentDate = paymentDate;
	}
	
	
}
